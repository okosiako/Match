int		match(char *s1, char *s2)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s2[b] == '*')
		b++;
	if (s2[b] == '\0')
		return (1);
	while (s1[a] != s2[b] && s1[a] != '\0')
		a++;
	if (s1[a] == '\0')
		return (0);
	while (s1[a] == s2[b])
	{
		a++;
		b++;
	}
	if (s1[a] == '\0' && s2[b] == '\0')
		return (1);
	return (match(&s1[a], &s2[b]));
}
